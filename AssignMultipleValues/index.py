# Many Values to Multiple Variables

x, y, z = "Orange", "Banana", "Cherry"
print(x)
print(y)
print(z)


# One Value to Multiple Variables

a = b = c = "Strawberry"
print(a)
print(b)
print(c)

# Unpack a Collection

fruits = ["lime", "Lemon", "Grapefruit"]
d, e, f = fruits
print(d)
print(e)
print(f)